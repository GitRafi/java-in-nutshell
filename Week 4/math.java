public class math {
    public static void main(String[] args) {
        //Soal a = 4^3 + 5^2
        int hasil_1 = (4*4*4) + (5*5);
        System.out.println("Hasil dari soal a = " + hasil_1);
        //Soal b = 5! + 4!
        int hasil_2 = (5*4*3*2*1) + (4*3*2*1);
        System.out.println("Hasil dari soal b = " + hasil_2);
        //Soal c = (4^2 + 5^1)/5+4^2
        float hasil_3 = (4*4 + 5) / 5f + 4*4;
        System.out.println("Hasil dari soal c = " + hasil_3);
    }
}
