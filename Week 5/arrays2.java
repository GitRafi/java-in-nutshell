public class arrays2 {
    public static void main(String[] args) {
        int ages[] = new int[100];

        for(int i=0;i<100;i++){
            System.out.println(ages[i]);
        }

        System.out.println("-----------------Batas Percobaan-----------------");

        for(int i=0;i<ages.length;i++) {
            System.out.println(ages[i]);
        }

    }
}
